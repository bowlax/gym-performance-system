#!/usr/bin/env node
/**
 * Prove iOS GoTrueTokenRefresher against LIVE GoTrue (#17).
 *
 * 1. Establish Auth-path session (admin generateLink → verifyOtp) for the
 *    synthetic Phase C customer — same as prove-auth-user-dimension.mjs.
 * 2. Verify the Custom Access Token Hook is on (top-level claims).
 * 3. Drive the REAL Swift `GoTrueTokenRefresher.refresh` via xcodebuild,
 *    by writing a temporary compile-time fixture (simulator cannot read
 *    host JSON paths), then restoring the empty fixture.
 *
 * Usage:
 *   node scripts/prove-ios-refresh-live.mjs
 *
 * Does NOT set TEAMUP_OAUTH_*. Requires the Custom Access Token Hook ENABLED.
 */

import { createClient } from "@supabase/supabase-js";
import { spawnSync } from "node:child_process";
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { createRemoteJWKSet, jwtVerify, decodeJwt } from "jose";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const STATE_PATH = join(__dirname, ".ios-refresh-live-proof.json");
const FIXTURE_PATH = join(
  ROOT,
  "tests/utilities/LiveRefreshProofFixture.generated.swift",
);

function emptyFixtureSource() {
  return `import Foundation

/// Written by \`scripts/prove-ios-refresh-live.mjs\` for the live GoTrue refresh proof.
/// Keep empty in git so ordinary unit-test runs skip the live test.
enum LiveRefreshProofFixture {
    static let isConfigured = false
    static let supabaseURL = ""
    static let publishableKey = ""
    static let refreshToken = ""
    static let memberId = ""
    static let gymId = ""
    static let appRole = ""
}
`;
}

function writeLiveFixture({ url, anon, refreshToken }) {
  const esc = (s) => s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
  writeFileSync(
    FIXTURE_PATH,
    `import Foundation

/// AUTO-GENERATED by scripts/prove-ios-refresh-live.mjs — do not commit secrets.
enum LiveRefreshProofFixture {
    static let isConfigured = true
    static let supabaseURL = "${esc(url)}"
    static let publishableKey = "${esc(anon)}"
    static let refreshToken = "${esc(refreshToken)}"
    static let memberId = "${esc(MEMBER_ID)}"
    static let gymId = "${esc(WOLF_GYM_ID)}"
    static let appRole = "${esc(APP_ROLE)}"
}
`,
  );
}

function restoreEmptyFixture() {
  writeFileSync(FIXTURE_PATH, emptyFixtureSource());
}

const WOLF_GYM_ID = "0abc9301-b048-40f5-8bdc-9bb389916b59";
const SYNTHETIC_CUSTOMER_ID = "PHASE-C-CUSTOMER-001";
const MEMBER_ID = "c3333333-3333-4333-8333-333333333333";
const APP_ROLE = "member";

function syntheticAuthEmail(customerId) {
  const local = String(customerId).replace(/[^a-zA-Z0-9_-]/g, "_");
  return `teamup-${local}@auth.gymperf.local`;
}

function loadEnvFile(path) {
  if (!existsSync(path)) return {};
  const out = {};
  for (const line of readFileSync(path, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
    const i = trimmed.indexOf("=");
    out[trimmed.slice(0, i)] = trimmed.slice(i + 1).trim().replace(/^['"]|['"]$/g, "");
  }
  return out;
}

function env() {
  const sb = loadEnvFile(join(ROOT, "supabase/.env.local"));
  const web = loadEnvFile(join(ROOT, "src/client/web/member-surface/.env.local"));
  const url = web.GYMPERF_SUPABASE_URL || sb.SUPABASE_URL;
  const serviceRole = sb.SERVICE_ROLE_KEY;
  const anon = web.GYMPERF_SUPABASE_PUBLISHABLE_KEY;
  if (!url || !serviceRole || !anon) {
    throw new Error("Missing URL / SERVICE_ROLE_KEY / publishable key");
  }
  return { url, serviceRole, anon };
}

function adminClient() {
  const { url, serviceRole } = env();
  return createClient(url, serviceRole, {
    auth: { persistSession: false, autoRefreshToken: false },
  });
}

function anonClient() {
  const { url, anon } = env();
  return createClient(url, anon, {
    auth: { persistSession: false, autoRefreshToken: false },
  });
}

async function ensureMember(admin) {
  const email = syntheticAuthEmail(SYNTHETIC_CUSTOMER_ID);
  const app_metadata = {
    member_id: MEMBER_ID,
    gym_id: WOLF_GYM_ID,
    app_role: APP_ROLE,
  };

  const { error: upsertErr } = await admin.from("members").upsert(
    {
      id: MEMBER_ID,
      gym_id: WOLF_GYM_ID,
      teamup_customer_id: SYNTHETIC_CUSTOMER_ID,
      display_name: "Phase C Auth-user Proof",
    },
    { onConflict: "id" },
  );
  if (upsertErr) throw upsertErr;

  const { data: row, error: readErr } = await admin
    .from("members")
    .select("id, auth_user_id")
    .eq("id", MEMBER_ID)
    .single();
  if (readErr) throw readErr;

  let user;
  if (row.auth_user_id) {
    const got = await admin.auth.admin.getUserById(row.auth_user_id);
    if (got.error) throw got.error;
    user = got.data.user;
    const updated = await admin.auth.admin.updateUserById(user.id, { app_metadata });
    if (updated.error) throw updated.error;
    user = updated.data.user;
    console.log("Reused Auth user via members.auth_user_id:", user.id);
  } else {
    const created = await admin.auth.admin.createUser({
      email,
      email_confirm: true,
      app_metadata,
    });
    if (created.error) {
      if (!/already|registered|exists/i.test(created.error.message ?? "")) {
        throw created.error;
      }
      const link = await admin.auth.admin.generateLink({ type: "magiclink", email });
      if (link.error) throw link.error;
      user = link.data.user;
      const updated = await admin.auth.admin.updateUserById(user.id, { app_metadata });
      if (updated.error) throw updated.error;
      user = updated.data.user;
      console.log("Adopted Auth user by email:", user.id);
    } else {
      user = created.data.user;
      console.log("Created Auth user:", user.id);
    }
    const { error: linkErr } = await admin
      .from("members")
      .update({ auth_user_id: user.id })
      .eq("id", MEMBER_ID);
    if (linkErr) throw linkErr;
  }

  return { user, email };
}

async function establishSession(admin, anon, email) {
  const link = await admin.auth.admin.generateLink({ type: "magiclink", email });
  if (link.error) throw link.error;
  const tokenHash = link.data.properties?.hashed_token;
  if (!tokenHash) throw new Error("missing hashed_token");
  const verified = await anon.auth.verifyOtp({ type: "email", token_hash: tokenHash });
  if (verified.error) throw verified.error;
  const session = verified.data.session;
  if (!session?.access_token || !session.refresh_token) {
    throw new Error("verifyOtp returned incomplete session");
  }
  return session;
}

function assertTopLevelClaims(claims, label) {
  console.log(`\n[${label}]`, JSON.stringify({
    member_id: claims.member_id ?? null,
    gym_id: claims.gym_id ?? null,
    app_role: claims.app_role ?? null,
    role: claims.role,
  }, null, 2));
  const ok =
    claims.member_id === MEMBER_ID &&
    claims.gym_id === WOLF_GYM_ID &&
    claims.app_role === APP_ROLE &&
    claims.role === "authenticated";
  if (!ok) {
    throw new Error(
      `${label}: missing top-level claims — enable Custom Access Token Hook in the dashboard`,
    );
  }
  console.log(`PASS [${label}]: top-level claims present`);
}

async function verifyEs256(accessToken) {
  const { url } = env();
  const jwks = createRemoteJWKSet(new URL(`${url}/auth/v1/.well-known/jwks.json`));
  await jwtVerify(accessToken, jwks);
  console.log("PASS: access_token verifies against project JWKS (ES256)");
}

function runSwiftLiveTest(refreshToken) {
  const { url, anon } = env();
  // Compile-time fixture — simulator XCTest cannot read host JSON paths.
  writeLiveFixture({ url, anon, refreshToken });

  console.log("\n=== Driving real GoTrueTokenRefresher via xcodebuild ===\n");
  console.log(`Fixture: ${FIXTURE_PATH}`);
  try {
    const result = spawnSync(
      "xcodebuild",
      [
        "test",
        "-project", "GymPerformance.xcodeproj",
        "-scheme", "GymPerformance",
        "-destination", "platform=iOS Simulator,name=iPhone 17,OS=26.5",
        "-only-testing:GymPerformanceTests/GoTrueTokenRefresherLiveTests",
      ],
      {
        cwd: ROOT,
        encoding: "utf8",
      },
    );

    const out = `${result.stdout ?? ""}\n${result.stderr ?? ""}`;
    const passLine = out.split("\n").filter((l) =>
      l.includes("GoTrueTokenRefresherLiveTests") ||
      l.includes("liveRefreshRotates") ||
      l.includes("TEST SUCCEEDED") ||
      l.includes("TEST FAILED") ||
      l.includes("✔ Test") ||
      l.includes("✘ Test") ||
      l.includes("Issue recorded") ||
      l.includes("Expected old refresh") ||
      l.includes("passed after") ||
      l.includes("failed after")
    );
    for (const line of passLine.slice(-50)) console.log(line);

    if (result.status !== 0 || !out.includes("TEST SUCCEEDED")) {
      throw new Error(`Swift live refresh test failed (exit ${result.status})`);
    }
    console.log("\nPASS: GoTrueTokenRefresher live test succeeded");
  } finally {
    restoreEmptyFixture();
    console.log("Restored empty LiveRefreshProofFixture (no secrets left in tree)");
  }
}

console.log("=== Prove iOS refresh against LIVE GoTrue ===\n");

const admin = adminClient();
const anon = anonClient();
const { user, email } = await ensureMember(admin);
const session = await establishSession(admin, anon, email);

const issuedClaims = decodeJwt(session.access_token);
assertTopLevelClaims(issuedClaims, "issued");
await verifyEs256(session.access_token);

writeFileSync(
  STATE_PATH,
  JSON.stringify({
    authUserId: user.id,
    email,
    memberId: MEMBER_ID,
    refreshTokenPrefix: session.refresh_token.slice(0, 8) + "…",
    provedAt: new Date().toISOString(),
  }, null, 2) + "\n",
);

runSwiftLiveTest(session.refresh_token);

console.log("\n=== done ===");
console.log(`State: ${STATE_PATH}`);
